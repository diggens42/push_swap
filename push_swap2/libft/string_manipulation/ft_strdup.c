#include "libft.h"

// creates a new dynamically allocated string
// contains same chars as input string

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(s1) + 1;
	copy = (char *)malloc(len);
	if (copy == NULL)
		return (NULL);
	ft_strlcpy(copy, s1, len);
	return (copy);
}
